const mongoose = require('mongoose');
const Schema = moongose.Schema;

const CourseSchema = new Schema({
  name: {
    type: String,
    unique: true,
    required: true,
  },
  description: {
    type: String,
    required: true,
    trim: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

const Course = moongose.model('Course', 'CourseSchema');

module.exports = Course;
